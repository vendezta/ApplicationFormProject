package com.tanatat.bean;

public class EducationInfomation {

	private String schoolnName;
	private String discipline;
	private String effAcademicYear;
	private String endAcademicYear;

	public String getSchoolnName() {
		return schoolnName;
	}

	public void setSchoolnName(String schoolnName) {
		this.schoolnName = schoolnName;
	}

	public String getDiscipline() {
		return discipline;
	}

	public void setDiscipline(String discipline) {
		this.discipline = discipline;
	}

	public String getEffAcademicYear() {
		return effAcademicYear;
	}

	public void setEffAcademicYear(String effAcademicYear) {
		this.effAcademicYear = effAcademicYear;
	}

	public String getEndAcademicYear() {
		return endAcademicYear;
	}

	public void setEndAcademicYear(String endAcademicYear) {
		this.endAcademicYear = endAcademicYear;
	}

}
